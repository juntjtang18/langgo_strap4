'use strict';

const { Client } = require('pg');

const getConnectionConfig = () => ({
  host: process.env.DATABASE_HOST,
  port: Number(process.env.DATABASE_PORT || 5432),
  user: process.env.DATABASE_USERNAME,
  password: process.env.DATABASE_PASSWORD,
  database: process.env.DATABASE_NAME,
});

const buildTranslationsForCode = (code) => {
  const createMatch = code.match(/^FLASHCARD_CREATE_(\d+)$/);
  if (createMatch) {
    const count = createMatch[1];
    return {
      en: {
        title: `Create ${count} Flashcards`,
        description: `Create ${count} flashcards in LangGo.`,
      },
      zh: {
        title: `创建${count}张闪卡`,
        description: `在 LangGo 中创建${count}张闪卡。`,
      },
    };
  }

  const reviewMatch = code.match(/^FLASHCARD_REVIEW_(\d+)$/);
  if (reviewMatch) {
    const count = reviewMatch[1];
    return {
      en: {
        title: `Complete ${count} Reviews`,
        description: `Complete ${count} flashcard reviews in LangGo.`,
      },
      zh: {
        title: `完成${count}次复习`,
        description: `在 LangGo 中完成${count}次闪卡复习。`,
      },
    };
  }

  const rememberedMatch = code.match(/^FLASHCARD_REMEMBERED_(\d+)$/);
  if (rememberedMatch) {
    const count = rememberedMatch[1];
    return {
      en: {
        title: `Remember ${count} Flashcards`,
        description: `Remember ${count} flashcards in LangGo.`,
      },
      zh: {
        title: `记住${count}张闪卡`,
        description: `在 LangGo 中记住${count}张闪卡。`,
      },
    };
  }

  return null;
};

const main = async () => {
  const client = new Client(getConnectionConfig());
  await client.connect();

  try {
    await client.query('BEGIN');

    const hasAchievementLocaleColumn = await client.query(`
      SELECT EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_schema = 'public'
          AND table_name = 'as_achievements'
          AND column_name = 'locale'
      ) AS exists
    `);

    await client.query(`
      CREATE TABLE IF NOT EXISTS public.as_achievement_translations (
        id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        locale character varying(255),
        title character varying(255),
        description text,
        created_at timestamp(6) without time zone,
        updated_at timestamp(6) without time zone,
        created_by_id integer REFERENCES public.admin_users(id) ON DELETE SET NULL,
        updated_by_id integer REFERENCES public.admin_users(id) ON DELETE SET NULL
      )
    `);

    await client.query(`
      CREATE INDEX IF NOT EXISTS as_achievement_translations_created_by_id_fk
      ON public.as_achievement_translations (created_by_id)
    `);
    await client.query(`
      CREATE INDEX IF NOT EXISTS as_achievement_translations_updated_by_id_fk
      ON public.as_achievement_translations (updated_by_id)
    `);

    await client.query(`
      CREATE TABLE IF NOT EXISTS public.as_achievement_translations_achievement_links (
        id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        as_achievement_translation_id integer REFERENCES public.as_achievement_translations(id) ON DELETE CASCADE,
        as_achievement_id integer REFERENCES public.as_achievements(id) ON DELETE CASCADE,
        as_achievement_translation_order double precision
      )
    `);

    await client.query(`
      CREATE INDEX IF NOT EXISTS as_achievement_translations_achievement_links_fk
      ON public.as_achievement_translations_achievement_links (as_achievement_translation_id)
    `);
    await client.query(`
      CREATE INDEX IF NOT EXISTS as_achievement_translations_achievement_links_inv_fk
      ON public.as_achievement_translations_achievement_links (as_achievement_id)
    `);
    await client.query(`
      CREATE INDEX IF NOT EXISTS as_achievement_translations_achievement_links_order_inv_fk
      ON public.as_achievement_translations_achievement_links (as_achievement_translation_order)
    `);

    await client.query(`
      DO $$
      BEGIN
        IF NOT EXISTS (
          SELECT 1 FROM pg_constraint
          WHERE conname = 'as_achievement_translations_achievement_links_unique'
        ) THEN
          ALTER TABLE public.as_achievement_translations_achievement_links
          ADD CONSTRAINT as_achievement_translations_achievement_links_unique
          UNIQUE (as_achievement_translation_id, as_achievement_id);
        END IF;
      END $$;
    `);

    await client.query(`
      CREATE TABLE IF NOT EXISTS public.as_achievement_translations_localizations_links (
        id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        as_achievement_translation_id integer REFERENCES public.as_achievement_translations(id) ON DELETE CASCADE,
        inv_as_achievement_translation_id integer REFERENCES public.as_achievement_translations(id) ON DELETE CASCADE,
        as_achievement_translation_order double precision
      )
    `);

    await client.query(`
      CREATE INDEX IF NOT EXISTS as_achievement_translations_localizations_links_fk
      ON public.as_achievement_translations_localizations_links (as_achievement_translation_id)
    `);
    await client.query(`
      CREATE INDEX IF NOT EXISTS as_achievement_translations_localizations_links_inv_fk
      ON public.as_achievement_translations_localizations_links (inv_as_achievement_translation_id)
    `);

    await client.query(`
      DO $$
      BEGIN
        IF NOT EXISTS (
          SELECT 1 FROM pg_constraint
          WHERE conname = 'as_achievement_translations_localizations_links_unique'
        ) THEN
          ALTER TABLE public.as_achievement_translations_localizations_links
          ADD CONSTRAINT as_achievement_translations_localizations_links_unique
          UNIQUE (as_achievement_translation_id, inv_as_achievement_translation_id);
        END IF;
      END $$;
    `);

    if (hasAchievementLocaleColumn.rows[0].exists) {
      await client.query(`
        WITH canonical AS (
          SELECT DISTINCT ON (code)
            id, code
          FROM public.as_achievements
          ORDER BY code, CASE WHEN locale = 'en' THEN 0 ELSE 1 END, id
        ),
        localized AS (
          SELECT a.*
          FROM public.as_achievements a
          JOIN canonical c ON c.code = a.code
        )
        INSERT INTO public.as_achievement_translations (
          locale, title, description, created_at, updated_at, created_by_id, updated_by_id
        )
        SELECT
          coalesce(locale, 'en'),
          title,
          description,
          created_at,
          updated_at,
          created_by_id,
          updated_by_id
        FROM localized l
        WHERE l.title IS NOT NULL
          AND NOT EXISTS (
            SELECT 1
            FROM public.as_achievement_translations t
            JOIN public.as_achievement_translations_achievement_links atl
              ON atl.as_achievement_translation_id = t.id
            JOIN canonical c ON c.id = atl.as_achievement_id
            WHERE c.code = l.code
              AND coalesce(t.locale, 'en') = coalesce(l.locale, 'en')
          )
      `);

      await client.query(`
        WITH canonical AS (
          SELECT DISTINCT ON (code)
            id, code
          FROM public.as_achievements
          ORDER BY code, CASE WHEN locale = 'en' THEN 0 ELSE 1 END, id
        ),
        translation_source AS (
          SELECT
            c.id AS canonical_id,
            l.code,
            coalesce(l.locale, 'en') AS locale,
            t.id AS translation_id
          FROM public.as_achievements l
          JOIN canonical c ON c.code = l.code
          JOIN public.as_achievement_translations t
            ON coalesce(t.locale, 'en') = coalesce(l.locale, 'en')
           AND t.title IS NOT DISTINCT FROM l.title
           AND t.description IS NOT DISTINCT FROM l.description
        )
        INSERT INTO public.as_achievement_translations_achievement_links (
          as_achievement_translation_id,
          as_achievement_id,
          as_achievement_translation_order
        )
        SELECT translation_id, canonical_id, 1
        FROM translation_source ts
        WHERE NOT EXISTS (
          SELECT 1
          FROM public.as_achievement_translations_achievement_links links
          WHERE links.as_achievement_translation_id = ts.translation_id
            AND links.as_achievement_id = ts.canonical_id
        )
      `);

      await client.query(`
        WITH pairs AS (
          SELECT
            en_t.id AS en_id,
            non_en_t.id AS non_en_id
          FROM public.as_achievement_translations_achievement_links en_link
          JOIN public.as_achievement_translations en_t
            ON en_t.id = en_link.as_achievement_translation_id
          JOIN public.as_achievement_translations_achievement_links non_en_link
            ON non_en_link.as_achievement_id = en_link.as_achievement_id
          JOIN public.as_achievement_translations non_en_t
            ON non_en_t.id = non_en_link.as_achievement_translation_id
          WHERE coalesce(en_t.locale, 'en') = 'en'
            AND coalesce(non_en_t.locale, 'en') <> 'en'
        )
        INSERT INTO public.as_achievement_translations_localizations_links (
          as_achievement_translation_id,
          inv_as_achievement_translation_id,
          as_achievement_translation_order
        )
        SELECT id_left, id_right, 1
        FROM (
          SELECT en_id AS id_left, non_en_id AS id_right FROM pairs
          UNION ALL
          SELECT non_en_id AS id_left, en_id AS id_right FROM pairs
        ) all_pairs
        WHERE NOT EXISTS (
          SELECT 1
          FROM public.as_achievement_translations_localizations_links links
          WHERE links.as_achievement_translation_id = all_pairs.id_left
            AND links.inv_as_achievement_translation_id = all_pairs.id_right
        )
      `);

      await client.query(`
        WITH canonical AS (
          SELECT DISTINCT ON (code)
            id, code
          FROM public.as_achievements
          ORDER BY code, CASE WHEN locale = 'en' THEN 0 ELSE 1 END, id
        )
        UPDATE public.as_user_achievements_achievement_links ua
        SET as_achievement_id = c.id
        FROM public.as_achievements a
        JOIN canonical c ON c.code = a.code
        WHERE ua.as_achievement_id = a.id
          AND a.id <> c.id
      `);

      await client.query(`
        WITH canonical AS (
          SELECT DISTINCT ON (code)
            id, code
          FROM public.as_achievements
          ORDER BY code, CASE WHEN locale = 'en' THEN 0 ELSE 1 END, id
        )
        DELETE FROM public.as_achievements a
        USING canonical c
        WHERE a.code = c.code
          AND a.id <> c.id
      `);

      await client.query(`
        DELETE FROM public.as_achievements_localizations_links
      `).catch(() => {});

      await client.query(`
        ALTER TABLE public.as_achievements
        DROP CONSTRAINT IF EXISTS as_achievements_code_unique
      `);
      await client.query(`
        DROP INDEX IF EXISTS public.as_achievements_code_unique
      `);
      await client.query(`
        DO $$
        BEGIN
          IF NOT EXISTS (
            SELECT 1 FROM pg_constraint
            WHERE conname = 'as_achievements_code_unique'
          ) THEN
            ALTER TABLE public.as_achievements
            ADD CONSTRAINT as_achievements_code_unique UNIQUE (code);
          END IF;
        END $$;
      `);

      await client.query(`
        ALTER TABLE public.as_achievements
        DROP COLUMN IF EXISTS locale,
        DROP COLUMN IF EXISTS title,
        DROP COLUMN IF EXISTS description
      `);

      await client.query(`
        DROP TABLE IF EXISTS public.as_achievements_localizations_links
      `);
    }

    const achievements = await client.query(`
      SELECT id, code, created_at, updated_at, created_by_id, updated_by_id
      FROM public.as_achievements
      ORDER BY id
    `);

    for (const achievement of achievements.rows) {
      const translations = buildTranslationsForCode(achievement.code);
      if (!translations) {
        continue;
      }

      const translationIds = {};

      for (const locale of ['en', 'zh']) {
        const translation = translations[locale];
        const existing = await client.query(`
          SELECT t.id
          FROM public.as_achievement_translations t
          JOIN public.as_achievement_translations_achievement_links l
            ON l.as_achievement_translation_id = t.id
          WHERE l.as_achievement_id = $1
            AND coalesce(t.locale, 'en') = $2
          LIMIT 1
        `, [achievement.id, locale]);

        if (existing.rowCount > 0) {
          translationIds[locale] = existing.rows[0].id;
          await client.query(`
            UPDATE public.as_achievement_translations
            SET title = $1,
                description = $2,
                updated_at = now()
            WHERE id = $3
          `, [translation.title, translation.description, translationIds[locale]]);
          continue;
        }

        const created = await client.query(`
          INSERT INTO public.as_achievement_translations (
            locale, title, description, created_at, updated_at, created_by_id, updated_by_id
          )
          VALUES ($1, $2, $3, $4, now(), $5, $6)
          RETURNING id
        `, [
          locale,
          translation.title,
          translation.description,
          achievement.created_at,
          achievement.created_by_id,
          achievement.updated_by_id,
        ]);

        translationIds[locale] = created.rows[0].id;

        await client.query(`
          INSERT INTO public.as_achievement_translations_achievement_links (
            as_achievement_translation_id,
            as_achievement_id,
            as_achievement_translation_order
          )
          VALUES ($1, $2, 1)
          ON CONFLICT DO NOTHING
        `, [translationIds[locale], achievement.id]);
      }

      if (translationIds.en && translationIds.zh) {
        await client.query(`
          INSERT INTO public.as_achievement_translations_localizations_links (
            as_achievement_translation_id,
            inv_as_achievement_translation_id,
            as_achievement_translation_order
          )
          VALUES ($1, $2, 1), ($2, $1, 1)
          ON CONFLICT DO NOTHING
        `, [translationIds.en, translationIds.zh]);
      }
    }

    const summary = await client.query(`
      SELECT
        (SELECT count(*) FROM public.as_achievements) AS achievements,
        (SELECT count(*) FROM public.as_achievement_translations) AS translations
    `);

    await client.query('COMMIT');
    console.log(JSON.stringify(summary.rows[0], null, 2));
  } catch (error) {
    await client.query('ROLLBACK');
    throw error;
  } finally {
    await client.end();
  }
};

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
